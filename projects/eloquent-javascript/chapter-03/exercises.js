////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  let ans = null;
  if (num1 > num2){
    ans = num2;
  } else{
    ans = num1;
  }
  return ans;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  let ans = false;
  if (num % 2 === 0){
    ans = true;
  }
  return ans;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  let ans = 0;
  for (let i = 0; i < str.length; i++){
    if (str.split('')[i] === char){
      ans++;
    }
  }
  return ans;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  return countChars(str, 'B');
}

