////////////////////////////////////////////////////////////////////////////////
// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
  let ans = [];
  if (step === undefined){
    step = 1;
  } else if (step < 0 ){
    return [];
  }
  if (start === end){
    return [];
  };
  for (let i = start; i <= end; i += step){
    ans.push(i);
  };
  return ans;
}

////////////////////////////////////////////////////////////////////////////////
// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(arr, total=0) {
  //base case
  if (arr.length === 0){
    return total;
  };
  //recursion
  return sum(arr.slice(1), total += arr[0])
}

////////////////////////////////////////////////////////////////////////////////
// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(arr, revArr=[]) {
  //base case
  if (arr.length === 0){
    return revArr;
  }
  //recursion
  revArr.unshift(arr[0]);
  return reverseArray(arr.slice(1), revArr);
}

////////////////////////////////////////////////////////////////////////////////
// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArrayInPlace(arr) {
  if (arr.length % 2 === 0){
    for (let i = 0; i < arr.length/2; i++){
      let temp = arr[i];
      arr[i] = arr[arr.length - 1 - i];
      arr[arr.length - 1 - i] = temp;
    };
  } else {
    for (let i = 0; i < Math.floor(arr.length / 2); i++){
      let temp = arr[i];
      arr[i] = arr[arr.length - 1 - i];
      arr[arr.length - 1 - i] = temp;
    };
  }
}

////////////////////////////////////////////////////////////////////////////////
// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function arrayToList(arr) {
  var rest = null;
  for (let i = arr.length - 1; i >= 0; i--){
    rest = {value: arr[i], rest };
  };
  return rest;
}

////////////////////////////////////////////////////////////////////////////////
// listToArray /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function listToArray(list, arr=[]) {
  //base case
  if (list.rest === null){
    arr.push(list.value);
    return arr;
  }
  //recursion
  arr.push(list.value);
  return listToArray(list.rest, arr);
}

////////////////////////////////////////////////////////////////////////////////
// prepend /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function prepend(element, list) {
  let newList = [...listToArray(list)];
  newList.unshift(element);
  return arrayToList(newList);
}

////////////////////////////////////////////////////////////////////////////////
// nth /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function nth(list, n) {
  //base case
  if (n === 0){
    return list.value;
  } else if (n < 0){
    return undefined;
  }
  //recursion
  return nth(list.rest, n - 1);
}

////////////////////////////////////////////////////////////////////////////////
// deepEqual ///////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function deepEqual(x, y) {
  if (typeof(x) === 'object' && typeof(y) === 'object'){
    let xKeys = Object.keys(x);
    let yKeys = Object.keys(y);
    if (xKeys.length !== yKeys.length){
      return false;
    }
    for (let i = 0; i < xKeys.length; i++){
      if (!yKeys.includes(xKeys[i]) || !deepEqual(x[xKeys[i]], y[yKeys[i]])){
        return false;
      }
    };
    return true;
  } else {
    return x === y;
  };
}

