function dominantDirection(str) {
  const arr = countBy(str, function(char){
    const unicode = char.charCodeAt();
    const script = characterScript(unicode);
    console.log(script);
    if (script !== null){
      return script.direction;
    } else {
      return script;
    };
  });
  arr.sort(function(a, b){
    return b.count - a.count;
  });
  return arr[0].name;
}

